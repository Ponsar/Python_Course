# if-else Statement
# Often, you'll want to take one action when a condition test passes and a different action in all other cases. Python's if-else syntax makes this possible.
# An if-else block is similar to a simple "if" statement, but the else statement allows you to define an action or set of actions that are executed when the conditional test fails.
age = 17 
if age >= 18:
    print("You are old enough to vote!")
    print("Have you registered to vote yet?")
else:
    print("Sorry, you are too young to vote.")
    print("Please register to vote as soon as you turn 18!")